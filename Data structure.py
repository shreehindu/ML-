# List
my_list = [1, 2, 3, "Python"]
print("List:", my_list, "- Mutable, Ordered")

# Tuple
my_tuple = (10, 20, "AI")
print("Tuple:", my_tuple, "- Immutable, Ordered")

# Set
my_set = {10, 20, 30, 40}
print("Set:", my_set, "- Unordered, Unique values")

# Dictionary
my_dict = {"name": "Machine Learning", "code": 101}
print("Dictionary:", my_dict, "- Key-Value pairs")
